			            <h2>About</h2>
			            <p>The Activity and Service, Accounting and Budget Office, also known as ASAB, consist of a team of professional administrators and a team of student specialist who manage the Activity and Service Fee. ASAB coordinates the A&amp;S Fee budget process, performs financial transaction processing, and facilitates a range of services to include the Student Travel processing and fiscal trainings.</p>
